// Nhập vào 2 số nguyên a, b từ bàn phím
var a = parseInt(prompt('Nhap a ')); 
var b = parseInt(prompt('Nhập b')) ;
//Tính tổng c của a và b (c = a + b)
var c = a + b;
//In ra kết quả
alert('C = '+c);
document.write(c);
// Nhập từ bàn phím d
var d = parseInt(prompt('Nhap d :'));
// tính tổng c+d
var result = c+d;
// in ra kết quả
alert('result :'+result);
